#include<iostream>
using namespace std;

int main()
{
    float a,b;

    cout<<"Enter Two Float Values: "<<endl;

    cin>>a>>b;
    //>>: Extraction operator, used for input streams like cin


    cout<<"The Values are: "<<a<<" and "<<b;
    //<<: Insertion operator, used for output streams like cout

    return 0;

}
